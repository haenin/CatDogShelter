function imagesFor(id, max = 8) {
  const out = []
  const exts = ['jpg', 'jpeg', 'png', 'webp', 'gif']
  for (let i = 1; i <= max; i++) {
    let found = null
    for (const ext of exts) {
      const name = `${id}-${i}.${ext}`
      if (IMG[name]) { found = IMG[name]; break }
    }
    if (!found) break
    out.push(found)
  }
  return out
}

